package Flow_control;

public class For_ {
    public static void main(String[] args) {
        // for 문
        // 다양한 치즈의 종류를 for문으로 출력
        String[] cheese = {"체다", "고다", "모짜렐라",
        "파마산"};
        for (int i = 0; i < cheese.length; i++) {
            System.out.println(cheese[i]);
        }
    }
}
